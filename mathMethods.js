
// 1.Math.abs()
const absValue = Math.abs(-15);
console.log('Absolute value:', absValue);

// 2.Math.ceil()
const ceilValue = Math.ceil(3.5);
console.log('Ceil:', ceilValue);

// 3.Math.floor()
const floorValue = Math.floor(-2.8)
console.log('Floor:', floorValue);

// 4.Math.round()
const roundValue = Math.round(4.4);
console.log('Round:', roundValue);

// 5.Math.Max()
const maxNumber = Math.max(10, 20, 30, 40,100);
console.log('Max:', maxNumber);

// 6.Math.min()
const minNumber = Math.min(2.5,10, 20, 30, 40);
console.log('Min:', minNumber);

// 7.Math.pow()
const powerValue = Math.pow(2, 4); 
console.log('Power:', powerValue);

// 8.Math.sqrt()
const sqrtValue = Math.sqrt(25);
console.log('Square root:', sqrtValue);

// 9.Math.trunc() 
const truncValue = Math.trunc(1.13);
console.log('Truncate:', truncValue);

// 10.Math.random()
const randomNumber = Math.random();
console.log('Random number:', randomNumber);

